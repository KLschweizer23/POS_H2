    private void createColumns(){
        dtm = new DefaultTableModel(0,0){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        table_transfer.setModel(dtm);
        dtm.addColumn("ID");
        dtm.addColumn("Store");
        dtm.addColumn("Date");
    }
    
    private void prepareTable(){
        DB_TransferStocks tsDb = new DB_TransferStocks();
        
        dtm.setRowCount(0);
        
        HashMap<String, StockTransfer> stockTransfers = tsDb.processData("", 0);
        ArrayList<String> idLists = tsDb.getIdList();
        
        for(int i = idLists.size() - 1; i >= 0; i--){
            String id = idLists.get(i);
            String[] rowData = {
                stockTransfers.get(id).getId(),
                stockTransfers.get(id).getStoreId(),
                stockTransfers.get(id).getDate()
            };
            dtm.addRow(rowData);
        }
        
        if(table_transfer.getRowCount() > 0)
            table_transfer.setRowSelectionInterval(0, 0);
        
        table_transfer.setRowHeight(30);
        label_transferCount.setText(stockTransfers.size() + " transfer/s");
    }
